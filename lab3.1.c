#include <stdio.h>




int main(void) {
	
	double length;
	length = 1;
	printf("length is %f meters \n", length);
	printf("length is %f centimeters \n", length*100);
	printf("length is %f myriameters \n", length*0.0001);
	printf("length is %f inches \n", length*39.3701);
	printf("length is %f miles \n", length*0.000621371);
	printf("length is %f furlongs \n", length*0.00497096);	
	return 0;

	}
